/**
 * Preprocessing script — run once to regenerate src/court-images.ts
 *
 * Reads the 16 original base64 PNG court images from git (they were in
 * main.ts as COURT_IMAGES before the refactor). Splits each at row 69
 * (top/bottom halves, each 85×69 px, within the G2's 100px image container
 * height limit), deduplicates identical halves, adds a solid-black BLANK_HALF,
 * and emits src/court-images.ts with base64 PNG strings the G2 firmware can
 * decode.
 *
 * Usage: node scripts/split-court-images.mjs
 * Requires: pngjs (already installed as devDependency)
 */

import pkg from 'pngjs'
const { PNG } = pkg

import { Buffer } from 'buffer'
import { writeFileSync } from 'fs'
import { execSync } from 'child_process'
import { fileURLToPath } from 'url'
import { join, dirname } from 'path'

const __dir = dirname(fileURLToPath(import.meta.url))
const repoRoot = join(__dir, '../..')

// ---------------------------------------------------------------------------
// Extract COURT_IMAGES from git history (removed from main.ts during refactor)
// ---------------------------------------------------------------------------

const diff = execSync('git diff HEAD g2-app/src/main.ts', { cwd: repoRoot }).toString()
const imageRegex = /^\-\s+'([\w-]+)':\s+'([A-Za-z0-9+/=]+)'/gm
const COURT_IMAGES = {}
let m
while ((m = imageRegex.exec(diff)) !== null) {
  COURT_IMAGES[m[1]] = m[2]
}
if (Object.keys(COURT_IMAGES).length === 0) {
  throw new Error('No COURT_IMAGES found in git diff. Run this script before committing the COURT_IMAGES removal.')
}
console.log(`Found ${Object.keys(COURT_IMAGES).length} images from git diff`)

const IMAGE_WIDTH  = 85
const IMAGE_HEIGHT = 138
const HALF_HEIGHT  = 69
const SPLIT_ROW    = 69

// ---------------------------------------------------------------------------
// Decode PNG → greyscale pixel array (1 byte per pixel, R channel)
// ---------------------------------------------------------------------------

function decodePng(base64) {
  const buf = Buffer.from(base64, 'base64')
  const png = PNG.sync.read(buf)
  if (png.width !== IMAGE_WIDTH || png.height !== IMAGE_HEIGHT) {
    throw new Error(`Unexpected image size: ${png.width}×${png.height}`)
  }
  const grey = new Uint8Array(IMAGE_WIDTH * IMAGE_HEIGHT)
  for (let i = 0; i < grey.length; i++) grey[i] = png.data[i * 4]
  return grey
}

// ---------------------------------------------------------------------------
// Re-encode greyscale pixels as base64 PNG
// ---------------------------------------------------------------------------

function toPngBase64(pixels, width, height) {
  const png = new PNG({ width, height, colorType: 0 })
  png.data = Buffer.alloc(width * height * 4)
  for (let i = 0; i < pixels.length; i++) {
    png.data[i * 4 + 0] = pixels[i]
    png.data[i * 4 + 1] = pixels[i]
    png.data[i * 4 + 2] = pixels[i]
    png.data[i * 4 + 3] = 255
  }
  return PNG.sync.write(png).toString('base64')
}

// ---------------------------------------------------------------------------
// Split and deduplicate
// ---------------------------------------------------------------------------

const halves = []
const halfKeyMap = new Map()

function getOrAddHalf(data) {
  const key = Buffer.from(data).toString('hex')
  if (halfKeyMap.has(key)) return halfKeyMap.get(key)
  const idx = halves.length
  halves.push(toPngBase64(data, IMAGE_WIDTH, HALF_HEIGHT))
  halfKeyMap.set(key, idx)
  return idx
}

const indices = {}

for (const [key, b64] of Object.entries(COURT_IMAGES)) {
  const grey      = decodePng(b64)
  const top       = grey.subarray(0, IMAGE_WIDTH * SPLIT_ROW)
  const bottom    = grey.subarray(IMAGE_WIDTH * SPLIT_ROW)
  const topIdx    = getOrAddHalf(top)
  const bottomIdx = getOrAddHalf(bottom)
  indices[key] = [topIdx, bottomIdx]
  console.log(`${key} → top=${topIdx} bottom=${bottomIdx}`)
}
console.log(`\n${Object.keys(COURT_IMAGES).length} images → ${halves.length} unique halves`)

// ---------------------------------------------------------------------------
// Generate blank (all-black) half
// ---------------------------------------------------------------------------

const blankPixels = Buffer.alloc(IMAGE_WIDTH * HALF_HEIGHT, 0)
const blankB64 = toPngBase64(blankPixels, IMAGE_WIDTH, HALF_HEIGHT)
console.log(`Blank half: ${blankB64.length} base64 PNG chars`)

// ---------------------------------------------------------------------------
// Emit src/court-images.ts
// ---------------------------------------------------------------------------

const halvesCode = halves
  .map((b64, i) => `  /* ${i} */ '${b64}'`)
  .join(',\n')

const indicesCode = Object.entries(indices)
  .map(([key, [t, b]]) => `  '${key}': [${t}, ${b}]`)
  .join(',\n')

const output = `\
// Auto-generated by scripts/split-court-images.mjs — do not edit manually.
// Each half is an 85×69 px greyscale PNG, base64-encoded.

export const SERVE_OPTIONS: Record<'us' | 'them', [string, string]> = {
  us:   ['left-back-2',  'right-serve-2'],
  them: ['left-net-2',   'right-receive-2'],
}

export const COURT_HALVES: string[] = [
${halvesCode}
]

export const COURT_HALF_INDICES: Record<string, [number, number]> = {
${indicesCode}
}

export const BLANK_HALF: string = '${blankB64}'
`

const outPath = join(dirname(fileURLToPath(import.meta.url)), '../src/court-images.ts')
writeFileSync(outPath, output, 'utf8')
console.log(`\nWrote ${outPath}`)
